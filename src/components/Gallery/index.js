class Gallery extends Component {
  state = {thumbnailId: 0}

  onClickThumbnail = id => {
    this.setState({thumbnailId: id})
  }

  render() {
    const {thumbnailId} = this.state
    const index = imagesList.findIndex(
      eachImage => eachImage.id === thumbnailId,
    )

    return (
      <div className="main-container">
        <div className="image-tab">
          <img
            src={imagesList[index].imageUrl}
            alt={imagesList[index].imageAltText}
          />
        </div>

        <div className="content-tab">
          <h1>Nature Photography</h1>
          <p>Nature photography by rahul</p>
          <ul>
            {imagesList.map(eachImage => {
              const {thumbnailUrl, id, thumbnailAltText} = eachImage
              return (
                <ThumbnailItem
                  thumbnailUrl={thumbnailUrl}
                  onClickThumbnail={this.onClickThumbnail}
                  id={id}
                  key={id}
                  thumbnailAltText={thumbnailAltText}
                />
              )
            })}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
